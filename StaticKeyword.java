public class StaticKeyword {
    // static variables and methods belong to the class rather than instances
    public static int staticvar1 = 10;
    public static int staticvar2 = 20;

    public static int staticSum(int a, int b) {
        return a + b;
    }

    public static int staticMultiply(int a, int b) {
        return a * b;
    }
}
